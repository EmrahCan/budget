// Banka arama fonksiyonu
export const searchBanks = (query) => {
  if (!query) return turkishBanks;
  
  const searchTerm = query.toLowerCase();
  return turkishBanks.filter(bank => 
    bank.name.toLowerCase().includes(searchTerm) ||
    bank.fullName.toLowerCase().includes(searchTerm)
  );
};

// Banka türüne göre filtreleme
export const getBanksByType = (type) => {
  return turkishBanks.filter(bank => bank.type === type);
};

// Banka ID'sine göre banka bulma
export const getBankById = (id) => {
  return turkishBanks.find(bank => bank.id === id);
};

